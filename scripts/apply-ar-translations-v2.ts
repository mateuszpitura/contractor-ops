function setDeep(obj: Record<string, unknown>, path: string, value: string): void {
  const parts = path.split('.');
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    const k = parts[i];
    const next = (cur as Record<string, unknown>)[k];
    if (typeof next !== 'object' || next === null || Array.isArray(next)) {
      (cur as Record<string, unknown>)[k] = {};
    }
    cur = (cur as Record<string, unknown>)[k] as Record<string, unknown>;
  }
  (cur as Record<string, unknown>)[parts[parts.length - 1]] = value;
}

