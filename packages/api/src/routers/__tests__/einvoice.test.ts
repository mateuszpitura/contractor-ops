describe('einvoice.complianceStatuses', () => {
  it('returns compliance statuses for KSeF profile with connection data', async () => {
    const ksefStatus = {
      profileId: 'ksef',
      status: 'active',
      label: 'KSeF',
      lastSync: new Date().toISOString(),
    };
    mockListProfiles.mockReturnValueOnce([{ profileId: 'ksef', getComplianceStatus: vi.fn() }]);
    mockPrisma.integrationConnection.findFirst.mockResolvedValueOnce({
      status: 'CONNECTED',
      configJson: { environment: 'production' },
      lastSyncAt: new Date(),
      lastSuccessAt: new Date(),
      lastErrorAt: null,
      lastErrorMessage: null,
      connectedAt: new Date(),
    });
    mockPrisma.integrationSyncLog.findMany.mockResolvedValueOnce([
      { status: 'SUCCESS' },
      { status: 'SUCCESS' },
    ]);
    mockComputeKsefComplianceStatus.mockReturnValueOnce(ksefStatus);

    const result = await caller.einvoice.complianceStatuses();

    expect(result.statuses).toHaveLength(1);
    expect(result.statuses[0]).toEqual(ksefStatus);
    expect(mockComputeKsefComplianceStatus).toHaveBeenCalledWith(
      expect.objectContaining({
        status: 'CONNECTED',
        recentSyncStatuses: ['SUCCESS', 'SUCCESS'],
      }),
    );
  });

  it('returns compliance status for KSeF when not connected', async () => {
    const disconnectedStatus = {
      profileId: 'ksef',
      status: 'disconnected',
      label: 'KSeF',
    };
    mockListProfiles.mockReturnValueOnce([{ profileId: 'ksef', getComplianceStatus: vi.fn() }]);
    mockPrisma.integrationConnection.findFirst.mockResolvedValueOnce(null);
    mockComputeKsefComplianceStatus.mockReturnValueOnce(disconnectedStatus);

    const result = await caller.einvoice.complianceStatuses();

    expect(result.statuses).toHaveLength(1);
    expect(mockComputeKsefComplianceStatus).toHaveBeenCalledWith(null);
  });

  it('delegates to profile getComplianceStatus for non-KSeF profiles', async () => {
    const mockGetComplianceStatus = vi.fn(async () => ({
      profileId: 'peppol',
      status: 'active',
      label: 'Peppol',
    }));
    mockListProfiles.mockReturnValueOnce([
      { profileId: 'peppol', getComplianceStatus: mockGetComplianceStatus },
    ]);

    const result = await caller.einvoice.complianceStatuses();

    expect(result.statuses).toHaveLength(1);
    expect(result.statuses[0]?.profileId).toBe('peppol');
    expect(mockGetComplianceStatus).toHaveBeenCalledWith(ORG_ID);
  });

  it('returns empty statuses when no profiles registered', async () => {
    mockListProfiles.mockReturnValueOnce([]);

    const result = await caller.einvoice.complianceStatuses();

    expect(result.statuses).toEqual([]);
  });
});
